#include <iostream>
#include <cmath>

using namespace std;

int main() {

    double a, b;

    cin >> a >> b;
    
    cout << round(a) << endl; 
    cout << ceil(a) << endl;
    cout << floor(a) << endl;

    cout << round(b) << endl; 
    cout << ceil(b) << endl;
    cout << floor(b) << endl;

    // https://www.w3schools.com/cpp/cpp_math.asp
    
    return 0;
}