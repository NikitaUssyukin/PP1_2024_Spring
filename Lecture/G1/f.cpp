#include <iostream>
#include <cmath>

using namespace std;

int main() {
    double a, b;

    cin >> a >> b;

    cout << pow(a, b) << endl;

    cout << abs(a) << endl;

    cout << sqrt(b) << endl;

    cout << round(a) << endl;

    cout << ceil(a) << endl;

    cout << floor(a) << endl;

    return 0;
}